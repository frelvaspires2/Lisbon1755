/// <summary>
/// Enum with all possible event states.
/// </summary>
public enum EventState 
{
    /// <summary>
    /// When the event hasn't started.
    /// </summary>
    Inactive,
    /// <summary>
    /// When the event has started.
    /// </summary>
    Active,
    /// <summary>
    /// When the event has finished.
    /// </summary>
    Finished,
}
