/// <summary>
/// Enum with all possible event results.
/// </summary>
public enum EventResult
{
    /// <summary>
    /// When there's no result (yet).
    /// </summary>
    None,
    /// <summary>
    /// If the player fails.
    /// </summary>
    Lost,
    /// <summary>
    /// If the player succeeds.
    /// </summary>
    Won,
}
